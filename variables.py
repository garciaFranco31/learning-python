#VARIABLES --> almacenan información útil y pueden ser modificadas a lo largo del tiempo.

nombre = "Franco" #variable declarada y definida

#concatenar consiste en unir dos o más strings

bienvenida = "Hola {nombre} ¿Cómo estás?"
print(bienvenida)

bienvenida = f"Hola {nombre} ¿Cómo estás?" #forma de concatenar los strings
print(bienvenida)

print("ola" in bienvenida) #le estoy pidiendo que busque si el string "ola" existe dentro de la variable


del bienvenida #borra la variable bienvenida
#del es una operación utilizada para borrar datos.
print (bienvenida)


