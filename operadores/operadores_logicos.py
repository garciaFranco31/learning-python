# & --> devuelve true si todos los valores de la condición tienen como resulado true, con que
# al menos una sea falsa, devuelve falso.

# | --> devuelve true con que al menos una condición tenga como resultado true. Devuelve false
# si y solo si, ambas condiciones tienen como resultado false.

# not --> complementa el valor de verdad

resultado = True & False # false
resultado = True | False # true
resultado = not True #false