#suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

#multiplicacion y división (* y /)
multiplicacion = 12 * 5
division = 12 / 5 #devuelve un dato float (flotante), aunque el resultado sea un entero

#potenciación (exponenete) (**)
exponente = 12**5

#división baja (//)
division_baja = 12//5

#modulo o resto (%)
modulo = 12 % 5

print(suma)
print(resta)
print(multiplicacion)
print(division)
print(exponente)
print(division_baja) #devuelve un entero redondeando hacia abajo
print(modulo)

#type nos devuelve el tipo de dato
tipo_de_dato = type(suma)


